     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        User::where('id',$id)->update(['password' => Hash::make(12345678) ]);
        return redirect('/users')->with('status',"Password $request->nama berhasil direset.!");
    }

    public function usersLevel(Request $request, $id)
    {
        User::where('id',$id)->update(['level' => $request->level ]);
        return redirect('/users')->with('status',"Hak akses $request->nama berhasil diubah.!");
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        User::where('id',$id)->delete();
        Pegawai::where('user_id',$id)->delete();
        return redirect('/users')->with('status',"Data berhasil dihapus.!");
    }
